    /**
     * Get the path to a demo template, creating it if needed.
     *
     * @param string $type 'simple' or 'table'.
     * @return string Path to the demo file.
     */
    public static function get_demo_path( $type ) {
        $dir = DG_PLUGIN_DIR . 'assets/demos/';
        wp_mkdir_p( $dir );

        $file = $dir . 'demo-' . $type . '.docx';

        if ( ! file_exists( $file ) ) {
            if ( 'simple' === $type ) {
                self::create_simple_demo( $file );
            } elseif ( 'table' === $type ) {
                self::create_table_demo( $file );
            }
        }

        return $file;
    }

